//Palabras A Frase;
//La función palabraAFrase debe retornar un string que contenga todos los elementos del array recibido.
//La función palabraAFrase recibe como parámetro un arreglo de strings.
//Asegúrarse de que el string retornado no tenga espacios adicionales al principio o al final.

function palabraAFrase(array){
    return array.join(' ').replace().trim();
}